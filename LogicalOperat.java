package OPERATORS;

public class LogicalOperat {
    public static void main(String[] args) {
        // Logical AND
        System.out.println((9>8) && (8>3));
        // Logical OR
        System.out.println((9<5) || (8<2));
        // Logical Not
        System.out.println(!(3>8));
    }
    
}
